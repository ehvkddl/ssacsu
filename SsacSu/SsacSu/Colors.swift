import UIKit

enum Colors {
    
    enum Brand {
        static let black = UIColor(named: "Black")
        static let error = UIColor(named: "Error")
        static let gray = UIColor(named: "Gray")
        static let inactive = UIColor(named: "Inactive")
        static let white = UIColor(named: "White")
        static let yellow = UIColor(named: "Yellow")
    }
    
    enum Text {
        static let primary = UIColor(named: "TXTPrimary")
        static let secondary = UIColor(named: "TXTSecondary")
    }
    
    enum Background {
        static let primary = UIColor(named: "BGPrimary")
        static let secondary = UIColor(named: "BGSecondary")
    }
    
    enum View {
        static let separator = UIColor(named: "Separator")
        static let alpha = UIColor(named: "Alpha")
    }
    
}
